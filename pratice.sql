-- part 2: revenue & sales analysis
-- business requirement: the finance department wants summarized revenue and sales insights.
-- q11. find total revenue generated by each category.
select p.category, sum(oi.quantity * oi.unit_price) as total_revenue
from order_items oi
join products p on oi.product_id = p.product_id
group by p.category;

-- q12. find total quantity sold for each product.
select p.product_name, sum(oi.quantity) as total_quantity
from order_items oi
join products p on oi.product_id = p.product_id
group by p.product_name;

-- q13. find total orders under each order status.
select status, count(*) as total_orders
from orders
group by status;

-- q14. find total revenue generated through each payment method.
select o.payment_method, sum(oi.quantity * oi.unit_price) as revenue
from order_items oi
join orders o on oi.order_id = o.order_id
group by o.payment_method;

-- q15. find average product price category-wise.
select category, avg(price) as avg_price
from products
group by category;

-- q16. find category-wise quantity sold.
select p.category, sum(oi.quantity) as total_quantity
from order_items oi
join products p on oi.product_id = p.product_id
group by p.category;

-- q17. find monthly order count.
select date_format(order_date, '%y-%m') as month,
       count(*) as total_orders
from orders
group by month
order by month;

-- q18. find monthly revenue trend.
select date_format(o.order_date, '%y-%m') as month,
       sum(oi.quantity * oi.unit_price) as revenue
from orders o
join order_items oi on o.order_id = oi.order_id
group by month
order by month;

-- q19. find the most commonly used payment method.
select payment_method
from orders
group by payment_method
order by count(*) desc
limit 1;

-- q20. find the highest revenue-generating category.
select p.category
from order_items oi
join products p on oi.product_id = p.product_id
group by p.category
order by sum(oi.quantity * oi.unit_price) desc
limit 1;

-- q21. find customers who placed more than 5 orders.
select customer_id, count(*) as order_count
from orders
