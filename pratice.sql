-- q28. find customers whose spending is above average customer spending.
select customer_id, total_spent
from (
    select o.customer_id,
           sum(oi.quantity * oi.unit_price) as total_spent
    from orders o
    join order_items oi on o.order_id = oi.order_id
    group by o.customer_id
) t
where total_spent > (
    select avg(total_spent)
    from (
        select sum(oi.quantity * oi.unit_price) as total_spent
        from orders o
        join order_items oi on o.order_id = oi.order_id
        group by o.customer_id
    ) avg_table
);

-- q29. find products contributing more than 10% of company revenue.
select p.product_name,
       sum(oi.quantity * oi.unit_price) as revenue
from order_items oi
join products p on oi.product_id = p.product_id
group by p.product_name
having revenue > (
    select 0.1 * sum(oi.quantity * oi.unit_price)
    from order_items oi
);

-- q30. find the city generating the highest revenue.
select c.city,
       sum(oi.quantity * oi.unit_price) as revenue
from customers c
join orders o on c.customer_id = o.customer_id
join order_items oi on o.order_id = oi.order_id
group by c.city
order by revenue desc
limit 1;